#Upper() method
a = "hello, world!"
print(a.upper())

#lower() method  return lower the string in lower case
a = "HELLO, WORLD!"
print(a.lower())

#strip() method  return a trimmed version of the string
a = "                     Hello, World! "
print(a.strip()) # returns "Hello, World!"

#replace() method replace one string with another 
a = "Hello, World!"
print(a.replace("H", "J"))

#split() method split string to substring
a = ("Hello, World!")
print(a.split(","))